function accum(s) {
  // your code
  let arr = [];
  let space = s.split("");
  for (let i = 0; i < space.length; i++) {
    //index +1 and mutiple it to find the amount it shows.
    // capitalize the first letter of the string
    arr.push(space[i].toUpperCase() + Array(i + 1).join(space[i].toLowercase()));

    console.log(space, "-------", arr)

  }
  return arr
}




